# Count the number of a particular letter in a sentence
string = "happy new year"
substring = "y"
count = string.count(substring)
print(f'The count is : {count}')

# Print Even Indexes of a Word
word = input("Enter a word: ")
modified_word = word[::2]
print(modified_word)

# Swap Cases of a Word
name = input("Enter a word: ")
modified_name = name.swapcase()
print(modified_name)
